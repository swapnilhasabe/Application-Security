package utility;

public class DbReturns {

	String userName;
	String fileName;
	byte[] fileEncKey;
	byte[] hashEncKey;
	String hashValue;
	String hhkey;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public byte[] getFileEncKey() {
		return fileEncKey;
	}
	public void setFileEncKey(byte[] fileEncKey) {
		this.fileEncKey = fileEncKey;
	}
	public byte[] getHashEncKey() {
		return hashEncKey;
	}
	public void setHashEncKey(byte[] hashEncKey) {
		this.hashEncKey = hashEncKey;
	}
	public String getHashValue() {
		return hashValue;
	}
	public void setHashValue(String hashValue) {
		this.hashValue = hashValue;
	}
	public String getHhkey() {
		return hhkey;
	}
	public void setHhkey(String hhkey) {
		this.hhkey = hhkey;
	}
	
	
	
	
}
